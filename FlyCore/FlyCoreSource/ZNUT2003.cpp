//----------------------------------------------------------------------------
// РАСЧЕТ ПОЛНОЙ НУТАЦИИ ПО IAU200A
void CalcNutationIAU200A(double JD2000, double *x)
{
    memset (x,0,2*sizeof (x[0]));
    NutationIAU200A_SL(JD2000, x) ;		// лунно-солнечная нутация в 1е-7 секунд дуги
    NutationIAU200A_Pl(JD2000, x) ;		// планетная нутация в 1е-7 секунд дуги
#ifndef AE_2003
	x[0]-= 417750+2996500*JD2000;
	x[1]-= 68192+252400*JD2000;
#endif
    x[0]/= 1e7;
    x[1]/= 1e7;
}


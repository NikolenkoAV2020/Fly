#include "ssp.h"

void mean(double xi,double *Mx, int *n)
{
double fn,fn1,c;

if((*n)<0)
  (*Mx)=0.0;
  else
  if((*n)==0)
    (*Mx)=xi;
    else
    {
    fn1=(double)(*n);
    (*n)++;
    fn=(double)(*n);
    c= ( (*Mx)*fn1 + xi)/fn;
    (*Mx)=c;
    }
return;
}

struct TNutationBuffer
{
    const int	 nUz ;
    const double Step ;
    double*		 Buf ;
    double		 Tn ;

    TNutationBuffer() ;
    ~TNutationBuffer() ;
};

//---------------------------------------------------------------------------
// задаем количество узлов nUz
// и шаг интерполяционного полинома в сутках Step
TNutationBuffer::TNutationBuffer() :
	nUz (12),
	Step(0.5),
	Buf (nullptr),
	Tn  (-1e300)
{
	Buf = new double[nUz*2];
};

TNutationBuffer::~TNutationBuffer()
{
	delete[] Buf ;
}

static TNutationBuffer* NutationBuffer ;

//---------------------------------------------------------------------------
// РАСЧЕТ НУТАЦИИ С БУФЕРИЗАЦИЕЙ И ПОЛИНОМАМИ
void NutationSmooth(double JD2000, double *x)
{
	  int k = 0;

    if (!NutationBuffer) NutationBuffer = new TNutationBuffer;
    double h = NutationBuffer->Step/36525.;
    if (JD2000<NutationBuffer->Tn || JD2000>NutationBuffer->Tn+h*(NutationBuffer->nUz-1))
    {

        NutationBuffer->Tn = JD2000-4*h;
        for (k=0; k<NutationBuffer->nUz; k++)
        {
            double  jd2000 = NutationBuffer->Tn+k*h;
            CalcNutationIAU200A(jd2000, NutationBuffer->Buf+2*k) ;
        }

        double  fac = 1,
                *z;

        for (k=1; k<NutationBuffer->nUz; k++)
        {
            for (int j=NutationBuffer->nUz-1; j>=k; j--)
            {
                z = NutationBuffer->Buf+2*(j-1);
                z[2] -= z[0];
                z[3] -= z[1];
            }
            z[0] /= fac;
            z[1] /= fac;
            fac *= k;
        }
        z[2] /= fac;
        z[3] /= fac;
    }

    double  X = (JD2000-NutationBuffer->Tn)/h,
            Xn = 1;

    memcpy (x,NutationBuffer->Buf,2*sizeof(x[0]));

    for (k=1; k<NutationBuffer->nUz; k++)
    {
        Xn *= X-k+1;
        x[0] += NutationBuffer->Buf[2*k]  *Xn;
        x[1] += NutationBuffer->Buf[2*k+1]*Xn;
    }
    return;
}

void NutationFreeBuf( )
{
    delete NutationBuffer ;
    NutationBuffer = nullptr ;
}

